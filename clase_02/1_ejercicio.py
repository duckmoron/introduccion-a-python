"""

Crea un programa que realice una suma, una resta, multiplicación y división,

a partir de dos números ingresados por el usuario, y luego mostrar el resultado por pantalla

"""

# Entrada

numero_1 = int(input("Primer numero: "))

numero_2 = int(input("Segundo numero: "))

# Operación

resultado = numero_1 + numero_2

# Salida

print(f"El resultado de la suma es {resultado}")